import turtle

vim_turtle = turtle.Turtle()
# This is a square
def square():

    vim_turtle.forward(100)
    vim_turtle.right(90)
    vim_turtle.forward(100)
    vim_turtle.right(90)
    vim_turtle.forward(100)
    vim_turtle.right(90)
    vim_turtle.forward(100)

square()
vim_turtle.forward(200)
square()
